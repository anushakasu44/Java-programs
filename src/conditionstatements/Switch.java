package conditionstatements;

public class Switch {

public static void main(String[] args) {
		// switch//
		
		int day = 3;
		
		switch(day) {  
		
		case 1:
			System.out.println("mon");
			break;
		
		case 2:
			System.out.println("tue");
			break;
		
		case 3: 
			System.out.println("wed");
			break;
		
		case 4:
			System.out.println("thu");
			break;
		
		default:
			System.out.println("default");
			break;
			
		
		}
		
	
		

	}
}


