package Operators;

public class ArithmeticOperator {

	public static void main(String[] args) {
		// Arithmetic operator = + - / % * using int and double//
				//Addition operation //
				int a = 3;
				int b = 4;
				int c = a + b;
				System.out.println(c);

				//division returns: coefficient of the division//
				double f = 6.45;
				double g = 5.5;
				System.out.println(f/g);
				
				// Modulus returns: remainder of the division//
			   
				int x = 30;
			    double z = 20.22;
			    double k = x % z;
			    System.out.println(k);

	}

}
