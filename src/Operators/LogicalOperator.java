package Operators;

public class LogicalOperator {

	public static void main(String[] args) {
		// Logical operator: used to do operation betweeen 2 boolean values//
		//AND &&, OR ||, NOT ! //
		
		boolean z = false ;
		boolean y = true ;
		System.out.println(z || y);
		
		
		boolean i = true ;
		boolean j = false ;
		System.out.println(i && j);
		
		
		boolean d = false ;
		boolean c = false ;
		System.out.println(!c);
	}
	

}
